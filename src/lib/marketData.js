// Get S&P 500 value for a specific month/year
export function getSP500Value(year, month = 12) {
  if (!SP500_MONTHLY_DATA[year] || !SP500_MONTHLY_DATA[year][month]) {
    return null;
  }
  return SP500_MONTHLY_DATA[year][month];
}

// Get current S&P 500 value (latest available)
export function getCurrentSP500Value() {
  // Return the most recent value (Oct 2025)
  return 6742.28;
}

// Calculate what an investment would be worth if invested in S&P 500
export function calculateMarketInvestmentValue(initialAmount, startYear, startMonth = 1, endYear = null, endMonth = null) {
  const startValue = getSP500Value(startYear, startMonth);
  
  // Use current date if no end date specified
  if (!endYear) {
    endYear = new Date().getFullYear();
    endMonth = new Date().getMonth() + 1;
  }
  
  const endValue = endYear === 2025 && endMonth >= 10 ? 
    getCurrentSP500Value() : 
    getSP500Value(endYear, endMonth);
  
  if (!startValue || !endValue) {
    return null;
  }
  
  const marketValue = initialAmount * (endValue / startValue);
  
  return {
    startValue,
    endValue,
    marketValue,
    totalReturn: ((endValue / startValue) - 1) * 100,
    annualizedReturn: (Math.pow(endValue / startValue, 1 / ((endYear - startYear) + (endMonth - startMonth) / 12)) - 1) * 100
  };
}